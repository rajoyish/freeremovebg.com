#!/usr/bin/env node
/**
 * Generates worker/region-map.js from src/i18n/languages.json so the Cloudflare
 * Worker and the Astro build always share one source of truth. Run via
 * `pnpm i18n:regionmap` (also wired into the build script).
 */
import { readFile, writeFile } from "node:fs/promises";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");

const DEFAULT_LANG = "en";
const LANG_COOKIE = "lang";

const languages = JSON.parse(
  await readFile(resolve(ROOT, "src/i18n/languages.json"), "utf8"),
);

const regionEntries = [];
for (const lang of languages) {
  if (lang.code === DEFAULT_LANG) continue;
  for (const country of lang.countries) {
    regionEntries.push(`  ${JSON.stringify(country)}: ${JSON.stringify(lang.code)},`);
  }
}

const codes = languages.map((l) => `  ${JSON.stringify(l.code)},`).join("\n");

const out = `// AUTO-GENERATED by scripts/gen-region-map.mjs — do not edit by hand.
// Run \`pnpm i18n:regionmap\` after changing src/i18n/languages.json.
//
// Mirrors REGION_MAP / SUPPORTED_CODES from src/i18n/config.ts so the Worker
// shares a single source of truth with the Astro build.

/** ISO 3166-1 country code -> language sub-route. */
export const regionMap = {
${regionEntries.join("\n")}
};

/** Every supported language code, English ("${DEFAULT_LANG}") included. */
export const supportedCodes = [
${codes}
];

/** The default language served at the site root. */
export const defaultLang = ${JSON.stringify(DEFAULT_LANG)};

/** Cookie that persists an explicit user language choice. */
export const langCookie = ${JSON.stringify(LANG_COOKIE)};
`;

await writeFile(resolve(ROOT, "worker/region-map.js"), out, "utf8");
console.log("✅ Wrote worker/region-map.js");
